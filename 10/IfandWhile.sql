


UPDATE SALESLT.PRODUCT
SET DiscontinuedDate=GETDATE()
WHERE PRODUCTID=6800;

IF @@ROWCOUNT<1
BEGIN
	PRINT 'PRODUCT WAS NOT FOUNT'
END
ELSE
BEGIN
	PRINT 'PRODUCT UPDATED';
END

