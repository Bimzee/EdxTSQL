DECLARE @CUSTID INT =1, @LNAME NVARCHAR(20);
WHILE @CUSTID<5
BEGIN
	SELECT @LNAME=LASTNAME FROM SALESLT.CUSTOMER
	WHERE CUSTOMERID=@CUSTID
	PRINT @LNAME;
	SET @CUSTID +=1;
END